class Sneaker {
  constructor(
    brandname,
    category,
    colorway,
    description,
    gender,
    imageLink,
    name,
    prices,
    releaseDate,
    retailPrice,
    shoe,
    shortDescription,
    sizes,
    sku,
    storeLink,
    urlKey,
    url
  ) {
    this.brandname = brandname;
    this.category = category;
    this.colorway = colorway;
    this.description = description;
    this.gender = gender;
    this.imageLink = imageLink;
    this.name = name;
    this.prices = prices;
    this.releaseDate = releaseDate;
    this.retailPrice = retailPrice;
    this.shoe = shoe;
    this.sizes = sizes;
    this.shortDescription = shortDescription;
    this.sku = sku;
    this.storeLink = storeLink;
    this.urlKey = urlKey;
    this.url = url;
  }
}

export default Sneaker;
